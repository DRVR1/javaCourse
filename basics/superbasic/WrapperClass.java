package basics.superbasic;

public class WrapperClass {
    public static void main(String[] args) {
        Integer a = new Integer(12);
        System.out.println(a);

        Integer b = 123;
        System.out.println(b);

        String c = "125";
        int d = Integer.parseInt(c);
        System.out.println(d);
    }
}
